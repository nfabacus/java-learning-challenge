public class Loops {
    public static void main (String[] args) {
        //    while loop is the same as javascript.

        // do while
        int counter = 0;
        do {
            System.out.println(counter + " I love Jave");
            counter ++;
        } while (counter < 15);

        // for loop

        for (int i = 1; i <=15; i++) {
            System.out.println(i + " I love Java.");
        }
    }
}
